    private void jbtnProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnProcurarActionPerformed
        if (jtxtID.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Campo em branco.");
        }else{
            try{
                jtxtNome.setText(Classe_Singleton.getInstance().vendedores.get(Integer.parseInt(jtxtID.getText())).getNome());
                
                jtxtRG.setText(Classe_Singleton.getInstance().vendedores.get(Integer.parseInt(jtxtID.getText())).getRg());
                
                jtxtCPF.setText(Classe_Singleton.getInstance().vendedores.get(Integer.parseInt(jtxtID.getText())).getCpf());
                
                jtxtSetor.setText(Classe_Singleton.getInstance().vendedores.get(Integer.parseInt(jtxtID.getText())).getSetor());
                
                jtxtCargo.setText(Classe_Singleton.getInstance().vendedores.get(Integer.parseInt(jtxtID.getText())).getCargo());
                
                jtxtSalario.setText(Float.toString(Classe_Singleton.getInstance().vendedores.get(Integer.parseInt(jtxtID.getText())).getSalario()));
                
                jtxtEspecialidade.setText(Classe_Singleton.getInstance().vendedores.get(Integer.parseInt(jtxtID.getText())).getEspecialidade());
                
                jtxtComissao.setText(Float.toString(Classe_Singleton.getInstance().vendedores.get(Integer.parseInt(jtxtID.getText())).getComissao()));
                
               
                
            }catch(IndexOutOfBoundsException ioobe){
                JOptionPane.showMessageDialog(this, "Vendedor inexistente.");
            }
        }
    }//GEN-LAST:event_jbtnProcurarActionPerformed

