    private void jbtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEditarActionPerformed
        String nome, rg, cpf, cargo, supervisor, setor, especialidade;
        float salario,comissao;

        if (!(jtxtNome.getText().equals("") || jtxtRG.getText().equals("")
            || jtxtCPF.getText().equals("") || jtxtSetor.getText().equals("")
            || jtxtCargo.getText().equals("") || jtxtComissao.getText().equals("")
            || jtxtEspecialidade.getText().equals("") || jtxtSalario.getText().equals(""))){

        nome = jtxtNome.getText();
        rg = jtxtRG.getText();
        cpf = jtxtCPF.getText();
        setor = jtxtSetor.getText();
        cargo = jtxtCargo.getText();
        especialidade= jtxtEspecialidade.getText();
        try{
            salario = Float.parseFloat(jtxtSalario.getText());
            comissao = Float.parseFloat(jtxtComissao.getText());
            Classe_Singleton.getInstance().vendedores.get(Integer.parseInt(jtxtID.getText())).setNome(nome);
            Classe_Singleton.getInstance().vendedores.get(Integer.parseInt(jtxtID.getText())).setRg(rg);
            Classe_Singleton.getInstance().vendedores.get(Integer.parseInt(jtxtID.getText())).setCpf(cpf);
            Classe_Singleton.getInstance().vendedores.get(Integer.parseInt(jtxtID.getText())).setSetor(setor);
            Classe_Singleton.getInstance().vendedores.get(Integer.parseInt(jtxtID.getText())).setCargo(cargo);
            Classe_Singleton.getInstance().vendedores.get(Integer.parseInt(jtxtID.getText())).setEspecialidade(especialidade);
            Classe_Singleton.getInstance().vendedores.get(Integer.parseInt(jtxtID.getText())).setSalario(salario);
            Classe_Singleton.getInstance().vendedores.get(Integer.parseInt(jtxtID.getText())).setComissao(comissao);
            JOptionPane.showMessageDialog(this, "Êxito na Edição.");
            jtxtCPF.setText("");
            jtxtCargo.setText("");
            jtxtEspecialidade.setText("");
            jtxtNome.setText("");
            jtxtRG.setText("");
            jtxtSalario.setText("");
            jtxtSetor.setText("");
            jtxtComissao.setText("");
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(this,"Salário ou Comissão inválidos : \n"+nfe);
        }
        }else{
            JOptionPane.showMessageDialog(this,"Campos em Branco.");

        }
    }//GEN-LAST:event_jbtnEditarActionPerformed

